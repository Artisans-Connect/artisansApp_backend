/**
 * Experiment orchestrator for the ArtisansK0nnect dispatch engine.
 *
 * Runs every experiment against the REAL production ranking code and writes:
 *   - results/experiment_results.json  (machine-readable, full numbers)
 *   - results/RESULTS.md               (human-readable summary the paper cites)
 *
 * All randomness is seeded (SEED below); rerunning yields identical output.
 * Run with:  npm run bench
 */

import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import { makeWorkers, makeJobs } from "./simDataset";
import { assertMatchesEngine, TRUE_WEIGHTS } from "./scoring";
import { runLatencyBench, formatLatency } from "./latencyBench";
import { runSimLadder, runHungarianGap, formatSimLadder, POLICIES } from "./simHarness";
import { runWeightSensitivity, formatSensitivity } from "./weightSensitivity";
import { round } from "./stats";

const SEED = 20260701;
const HERE = dirname(fileURLToPath(import.meta.url));
const OUT_DIR = join(HERE, "results");

function pct(x: number, dp = 1): string {
  return `${round(x * 100, dp)}%`;
}

function main(): void {
  // Guard: the parameterised scorer must equal the production engine.
  const guardPool = makeWorkers(SEED, 200);
  const guardJob = makeJobs(SEED, 1)[0]!;
  assertMatchesEngine(guardPool, guardJob);

  process.stdout.write("Running latency benchmark...\n");
  const latency = runLatencyBench(SEED);

  process.stdout.write("Running dispatch simulation ladder (30 reps)...\n");
  // 300 workers, 700 jobs, each accepting worker locked for 15 subsequent
  // arrivals — adequate supply with mild churn. Under adequate supply every
  // reasonable policy matches nearly all jobs (refuting the "+68% matches"
  // claim); the differentiation is in dispatch efficiency and fairness.
  const ladder = runSimLadder(SEED, 300, 700, 30, 15);

  process.stdout.write("Running greedy-vs-Hungarian gap (40 batches)...\n");
  const hungarian = runHungarianGap(SEED, 40, 30, 60);

  process.stdout.write("Running weight sensitivity + ablations...\n");
  const sensitivity = runWeightSensitivity(SEED, 40, 300);

  // ----- machine-readable -----
  mkdirSync(OUT_DIR, { recursive: true });
  const json = {
    meta: {
      seed: SEED,
      weights: TRUE_WEIGHTS,
      radiusStepsKm: [5, 10, 15, 25],
      maxRounds: 3,
      workersPerRound: 3,
      note: "Seeded synthetic data over a Greater-Kumasi bounding box. Reproducible via `npm run bench`.",
    },
    latency,
    dispatchLadder: ladder,
    hungarianGap: hungarian,
    weightSensitivity: sensitivity,
  };
  writeFileSync(join(OUT_DIR, "experiment_results.json"), JSON.stringify(json, null, 2));

  // ----- human-readable -----
  const full = ladder.find((p) => p.policy === "full")!;
  const nearest = ladder.find((p) => p.policy === "nearest")!;
  const random = ladder.find((p) => p.policy === "random")!;
  const lat5k = latency.find((l) => l.poolSize === 5000)!;

  const matchGainVsRandom = (full.matchRate.mean - random.matchRate.mean) / Math.max(random.matchRate.mean, 1e-9);
  const dispatchVsRandom =
    (random.dispatchPerMatch.mean - full.dispatchPerMatch.mean) / random.dispatchPerMatch.mean;
  const dispatchVsNearest =
    (nearest.dispatchPerMatch.mean - full.dispatchPerMatch.mean) / nearest.dispatchPerMatch.mean;

  const md = `# ArtisansK0nnect — Experiment Results

*Auto-generated by \`npm run bench\` (\`scripts/experiments/runAll.ts\`). Seed \`${SEED}\`. Every seeded result (dispatch ladder, Hungarian gap, weight sensitivity) is bit-for-bit reproducible across runs; only §1 latency wall-clock timings vary, as expected for a hardware/JIT measurement.*

All experiments run against the **real production ranking code**
(\`src/services/recommendationEngine.ts\`): distance **${TRUE_WEIGHTS.distance}** /
responseRate **${TRUE_WEIGHTS.responseRate}** / rating **${TRUE_WEIGHTS.rating}**,
verification as a ≤0.02 tiebreaker, a binary location-freshness filter, and a
new-artisan fairness slot. Data is seeded synthetic over a Greater-Kumasi
bounding box (not field data — see limitations).

## 1. Ranking latency

\`rankRecommendationCandidates\` timed over ${lat5k.reps} reps per pool size
(warm JIT). This replaces the previously unbacked "142 ms" claim.

${formatLatency(latency)}

**Headline:** median **${round(lat5k.ms.median, 2)} ms** to rank
**5,000** candidates (95% CI ±${round(lat5k.ms.ci95, 2)} ms; p99
${round(lat5k.ms.p99, 2)} ms).

## 2. Dispatch quality — graded baseline ladder

30 reps on identical seeded demand streams (300 workers, 700 jobs; an accepting
worker is locked for 15 subsequent arrivals — adequate supply with mild churn).
Metrics are behavioural and non-circular (no proximity+rating "satisfaction").
Note: **higher Gini = more concentrated (less equitable) load.**

${formatSimLadder(ladder)}

**Findings — the manuscript's "+68% matches" claim does not hold; the real,
defensible effect is dispatch *efficiency*, with an equity trade-off:**
- **Match rate is parity, not a win.** full **${pct(full.matchRate.mean)}** vs
  nearest **${pct(nearest.matchRate.mean)}** vs random **${pct(random.matchRate.mean)}**
  (full is **${pct(matchGainVsRandom)}** over random). When supply is adequate, any
  reasonable policy matches nearly everything — so the inflated match-rate claim is
  refuted, and the contribution must be argued on cost and equity instead.
- **Dispatch efficiency is the genuine mechanism:** the full model needs
  **${pct(dispatchVsNearest)}** fewer push notifications per match than nearest-only
  and **${pct(dispatchVsRandom)}** fewer than random — the response-rate signal
  steers dispatches toward workers who actually accept. This is the honest core claim.
- **Equity cost (this is a finding, not a flaw to hide):** load Gini
  **${round(full.utilGini.mean, 3)}** (full) vs **${round(nearest.utilGini.mean, 3)}**
  (nearest) vs **${round(random.utilGini.mean, 3)}** (random) — optimising for
  responsiveness *concentrates* work on the most reliable workers. The new-artisan
  fairness slot only buys a foothold; it does not equalise load. **This directly
  motivates the equity safeguard** (tiered verification / rotation) in the discussion.
- **Proximity trade-off:** mean realised travel **${round(full.meanMatchKm.mean, 2)} km**
  (full) vs **${round(nearest.meanMatchKm.mean, 2)} km** (nearest) — the full model
  gives up some closeness to buy reliability, an interpretable, defensible trade.

## 3. Greedy vs. optimal (Hungarian) batch matching

40 batches (30 jobs × 60 available workers each). Mean relative gap between the
deployed greedy per-job assignment and the Hungarian optimum:

**${pct(hungarian.mean, 2)}** (95% CI ±${pct(hungarian.ci95, 2)}; max ${pct(hungarian.max, 2)}).

A small gap means the greedy heuristic is near-optimal at these batch sizes —
justifying it as a low-compute design choice rather than a deficiency.

## 4. Weight sensitivity & ablations

${formatSensitivity(sensitivity)}

**Reading:** if ±20% perturbations move the top-3 cohort little, the exact
weights are not fragile; the ablation churn shows how load-bearing each signal
is (a signal whose removal barely changes the ranking is doing little work).

## Limitations

- Data is **seeded synthetic** over a bounding box, not field measurements.
  Absolute rates depend on the generative assumptions (accept-probability
  distribution, supply/demand ratio, freshness rate); the **relative ordering**
  of policies is the robust result.
- Latency is single-machine, single-threaded Node; absolute ms are
  hardware-dependent — report alongside the machine used.
`;

  writeFileSync(join(OUT_DIR, "RESULTS.md"), md);
  process.stdout.write(`\nWrote:\n  ${join(OUT_DIR, "experiment_results.json")}\n  ${join(OUT_DIR, "RESULTS.md")}\n`);
}

main();
